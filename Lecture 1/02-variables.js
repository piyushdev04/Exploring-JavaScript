/*
    Variables
    - Variables are containers for data.
*/

fullName = "Batman!";
age = 21;
price = 99.99;
x = null;
y = undefined;
// boolean
isFollow = true;
notFollow = false;
console.log(fullName + " " + age + " " + price + " " + x + " " + y + " "+ isFollow + " " + notFollow);


