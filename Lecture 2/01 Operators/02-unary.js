// Unary Operators

let a = 5;
let b = 2;

console.log("a = ", a, " & b = ", b);

// Increment
b++; // b + 1 [Post]
console.log("b = ", b);

console.log("++b", ++b); // [pre]
/*
    Post and Pre Increment
    Post: a++
    Pre: ++a
*/


// Decrement
a--; // a - 1 [Post]
console.log("a = ", a);

console.log("--a", --a);
/*
    Post and Pre Decrement
    Post: a--
    Pre: --a
*/