// Comparison Operator
let a = 5;
let b = 5; 

console.log("5 == 2", a == b); // even if we have string ex: "5" it will convert string -> number and check
console.log("5 != 2", a != b);

console.log("a === b", a === b); // for b = "5" it'll return false bcuz "===" it dosen't change type also
console.log("a !== b", a !== b);

console.log("5 > 3", a > b);
console.log("5 < 3", a < b);

console.log("5 <= 5", a <= b); // for b = 5
console.log("5 >= 5", a >= b);