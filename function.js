function sum(a , b){
    const sumValue = a + b;
    return sumValue;
}

function sub(a , b){
    const subValue = a - b;
    return subValue;
}

function mult(a , b){
    const sumValue = a * b;
    return sumValue;
}

function div(a , b){
    const subValue = a / b;
    return subValue;
}

const Value1 = sum(10 , 2)
const Value2 = sub(10 , 2)
const Value3 = mult(10 , 2)
const Value4 = div(10 , 2)

console.log(Value1);
